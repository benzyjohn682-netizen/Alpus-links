// Search function for countries
export const searchCountries = (query: string): Country[] => {
  if (!query.trim()) return countries
  
  const lowercaseQuery = query.toLowerCase()
  return countries.filter(country => 
    country.label.toLowerCase().includes(lowercaseQuery) ||
    country.value.toLowerCase().includes(lowercaseQuery) ||
    country.code.toLowerCase().includes(lowercaseQuery)
  )
}

// Get country by value
export const getCountryByValue = (value: string): Country | undefined => {
  return countries.find(country => country.value === value)
}
